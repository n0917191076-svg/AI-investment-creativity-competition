print("[LSTM T+1 方向預測]")

SEQ_LENGTH = 15
train_cutoff = all_test['Date'].min() - timedelta(days=1)

sc_lstm = StandardScaler()
X_lstm_all = sc_lstm.fit_transform(df_clean[feature_cols])
