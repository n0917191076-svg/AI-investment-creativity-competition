n_tr = len(df_clean[df_clean['Date'] <= train_cutoff])

def make_seq(X, y, seq_len):
    Xs, Ys = [], []
    for i in range(seq_len, len(X)):
        Xs.append(X[i-seq_len:i])
        Ys.append(y[i])
    return np.array(Xs), np.array(Ys)


X_s, y_s = make_seq(X_lstm_all, dirs_all, SEQ_LENGTH)

ns = n_tr - SEQ_LENGTH
sp = int(ns * 0.85)
Xtr, ytr = X_s[:sp], y_s[:sp]
Xva, yva = X_s[sp:ns], y_s[sp:ns]
Xte = X_s[ns:]

from sklearn.utils.class_weight import compute_class_weight
cw = compute_class_weight('balanced', classes=np.array([0,1]), y=ytr.astype(int))
class_weights = {0: cw[0], 1: cw[1]}

print(f"  訓練: {Xtr.shape} | 驗證: {Xva.shape} | 測試: {Xte.shape}")
print(f"  類別權重: 跌={cw[0]:.2f} 漲={cw[1]:.2f}")

model_lstm = Sequential([
    LSTM(48, return_sequences=True, input_shape=(SEQ_LENGTH, len(feature_cols))),
    BatchNormalization(), Dropout(0.3),
    LSTM(24),
    BatchNormalization(), Dropout(0.3),
    Dense(12, activation='relu'),
    Dropout(0.2),
    Dense(1, activation='sigmoid')
])
model_lstm.compile(optimizer=Adam(0.0005),
                   loss='binary_crossentropy',
                   metrics=['accuracy'])

cb = [EarlyStopping('val_loss', patience=20, restore_best_weights=True),
      ReduceLROnPlateau('val_loss', factor=0.5, patience=7, min_lr=1e-6)]

# 用 class_weight 處理不平衡
history = model_lstm.fit(Xtr, ytr, validation_data=(Xva, yva),
                         epochs=150, batch_size=64, callbacks=cb,
                         class_weight=class_weights, verbose=1)

# 驗證集最佳門檻
val_prob = model_lstm.predict(Xva, verbose=0).flatten()
best_t, best_a = 0.5, 0
for t in np.arange(0.35, 0.65, 0.01):
    a = np.mean((val_prob > t) == yva)
    if a > best_a:
        best_t, best_a = t, a
best_t = max(best_t, 0.45)
print(f"\n  最佳門檻: {best_t:.2f} (驗證準確率: {best_a*100:.1f}%)")

# 測試集預測
lstm_prob_arr = model_lstm.predict(Xte, verbose=0).flatten()
